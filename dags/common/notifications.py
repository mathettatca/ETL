"""Reusable Airflow notification callbacks."""

from __future__ import annotations

from html import escape
import logging
from typing import Any, Callable

from airflow.models import Variable
from airflow.utils.email import send_email

logger = logging.getLogger(__name__)


def _parse_recipients(value: str | None) -> list[str]:
    if not value:
        return []

    return [
        recipient.strip()
        for recipient in value.replace(";", ",").split(",")
        if recipient.strip()
    ]


def _get_failure_error(context: dict[str, Any], task_id: str) -> Any:
    error = context.get("failure_error") or context.get("error")
    if error:
        return error

    task_instance = context.get("task_instance") or context.get("ti")
    if task_instance is None:
        return None

    try:
        return task_instance.xcom_pull(task_ids=task_id, key="failure_error")
    except Exception:
        logger.exception("Failed to pull failure_error from XCom.")
        return None


def _html_cell(value: object) -> str:
    if value is None:
        return "N/A"
    return escape(str(value)).replace("\n", "<br>")


def _html_table(rows: list[tuple[str, object]]) -> str:
    body_rows = "".join(
        "<tr>"
        '<td style="width:180px;padding:10px 12px;border-bottom:1px solid #e2e8f0;'
        'background:#f8fafc;color:#475569;font-size:13px;font-weight:700;">'
        f"{_html_cell(label)}</td>"
        '<td style="padding:10px 12px;border-bottom:1px solid #e2e8f0;'
        'color:#0f172a;font-size:13px;">'
        f"{_html_cell(value)}</td>"
        "</tr>"
        for label, value in rows
    )
    return (
        '<table style="width:100%;border-collapse:collapse;border:1px solid #e2e8f0;'
        'border-radius:8px;overflow:hidden;background:#ffffff;">'
        f"<tbody>{body_rows}</tbody>"
        "</table>"
    )


