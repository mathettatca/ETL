def build_gmail_failure_callback(
    recipients_variable: str = "failure_email",
    default_recipients: str | None = "khoinm0603@gmail.com",
) -> Callable[[dict[str, Any]], None]:
    """Build an on_failure callback that sends email through Airflow SMTP/Gmail."""

    def _callback(context: dict[str, Any]) -> None:
        recipients = _parse_recipients(
            Variable.get(recipients_variable, default_var=default_recipients)
        )
        if not recipients:
            logger.warning(
                "Skip failure email because Airflow Variable '%s' is empty.",
                recipients_variable,
            )
            return

        dag = context.get("dag")
        dag_run = context.get("dag_run")
        task_instance = context.get("task_instance") or context.get("ti")
        exception = context.get("exception")

        dag_id = getattr(dag, "dag_id", None) or getattr(dag_run, "dag_id", "unknown")
        task_id = getattr(task_instance, "task_id", "dag")
        run_id = getattr(dag_run, "run_id", "unknown")
        logical_date = context.get("logical_date") or context.get("execution_date")
        failure_error = _get_failure_error(context, task_id) or exception

        subject = f"[Airflow][FAILED] {dag_id}.{task_id}"
        html_content = _build_failure_email_html(
            dag_id=str(dag_id),
            task_id=str(task_id),
            run_id=str(run_id),
            logical_date=logical_date,
            failure_error=failure_error,
        )

        send_email(
            to=recipients,
            subject=subject,
            html_content=html_content,
        )

    return _callback
